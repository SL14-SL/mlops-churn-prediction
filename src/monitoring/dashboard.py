def normalize_customer_id_column(df: pd.DataFrame) -> pd.DataFrame:
    """Normalize customer id column names to `customerid`."""
    df = df.copy()

    if "customerID" in df.columns and "customerid" not in df.columns:
        df = df.rename(columns={"customerID": "customerid"})

    return df


def load_prediction_log() -> pd.DataFrame | None:
    """Load and normalize the prediction log."""
    if not PREDICTION_LOG_FILE.exists():
        return None

    df = pd.read_parquet(PREDICTION_LOG_FILE)
    df = normalize_customer_id_column(df)

    if "prediction" in df.columns:
        df["churn_probability"] = pd.to_numeric(df["prediction"], errors="coerce")
        df["churn_prediction"] = (df["churn_probability"] >= 0.5).astype("Int64")

    if "expected_value" in df.columns:
        df["expected_profit"] = pd.to_numeric(df["expected_value"], errors="coerce")

    if "prediction_timestamp" in df.columns:
        df["prediction_time"] = pd.to_datetime(
            df["prediction_timestamp"],
            errors="coerce",
            utc=True,
        )

    return df


def load_ground_truth() -> pd.DataFrame | None:
    """Load cumulative churn ground truth labels."""
    if not CUMULATIVE_GT_FILE.exists():
        return None

    gt = pd.read_csv(CUMULATIVE_GT_FILE)
    gt = normalize_customer_id_column(gt)

    if "churn" in gt.columns:
        gt["churn"] = pd.to_numeric(gt["churn"], errors="coerce")

    return gt


def load_performance_history() -> pd.DataFrame | None:
    """Load churn performance history generated by run_performance_demo.py."""
    if not PERFORMANCE_HISTORY_FILE.exists():
        return None

    df = pd.read_parquet(PERFORMANCE_HISTORY_FILE)

    for col in ["timestamp", "window_start", "window_end"]:
        if col in df.columns:
            df[col] = pd.to_datetime(df[col], errors="coerce", utc=True)

    return df

def load_profit_curve() -> pd.DataFrame | None:
    """Load offline profit curve simulation."""
    if not PROFIT_CURVE_FILE.exists():
        return None

    return pd.read_parquet(PROFIT_CURVE_FILE)


def load_uplift_sensitivity() -> pd.DataFrame | None:
    """Load uplift sensitivity analysis."""
    if not UPLIFT_SENSITIVITY_FILE.exists():
        return None

    return pd.read_csv(UPLIFT_SENSITIVITY_FILE)


def attach_ground_truth(
    prediction_df: pd.DataFrame | None,
    ground_truth_df: pd.DataFrame | None,
) -> pd.DataFrame | None:
    """Attach churn labels to prediction rows using prediction_id."""
    if prediction_df is None:
        return None

    if ground_truth_df is None:
        return prediction_df

    if "prediction_id" not in prediction_df.columns:
        return prediction_df

    if "prediction_id" not in ground_truth_df.columns or "churn" not in ground_truth_df.columns:
        return prediction_df

    prediction_df = prediction_df.copy()

    if "churn" in prediction_df.columns:
        prediction_df = prediction_df.drop(columns=["churn"])

    return prediction_df.merge(
        ground_truth_df[["prediction_id", "churn"]],
        on="prediction_id",
        how="left",
    )


